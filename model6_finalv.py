# ── Statistical forecasters ────────────────────────────────────────────────
def croston_forecast(series: pd.Series, alpha: float = 0.1) -> float:
    """
    Croston (1972) method for intermittent demand.
    Separately smooths demand size (q) and inter-demand interval (p).
    Returns average demand per period = q / p.
    """
    nz = series[series > 0]
    if len(nz) < 2:
        return float(series.mean()) if series.mean() > 0 else 0.01
    q, p = float(nz.iloc[0]), 1.0
    for v in nz.iloc[1:]:
        q = alpha * float(v) + (1 - alpha) * q
        p = alpha * 1.0 + (1 - alpha) * p
    return max(q / p, 0.0)

def tsb_forecast(series: pd.Series, alpha: float = 0.1, beta: float = 0.1) -> float:
    """
    TSB (Teunter-Syntetos-Babai 2011) method for erratic demand.
    Tracks demand probability (p_t) separately from demand magnitude (z_t).
    Superior to Croston for erratic patterns — avoids obsolescence bias.
    Returns: p_t × z_t (expected demand per period).
    """
    arr = np.array(series, dtype=float)
    if len(arr) < 2:
        return float(arr.mean()) if arr.mean() > 0 else 0.01
    p_t = 0.5
    z_t = float(arr[arr > 0].mean()) if (arr > 0).any() else 0.01
    for v in arr:
        p_t = beta * (1.0 if v > 0 else 0.0) + (1 - beta) * p_t
        if v > 0:
            z_t = alpha * v + (1 - alpha) * z_t
    return max(p_t * z_t, 0.0)

def smooth_forecast(series: pd.Series) -> float:
    """
    Rolling mean fallback for smooth demand patterns.
    Uses last 28 days with a 7-day minimum window.
    """
    rolled = series.rolling(CONFIG['rolling_window'],
                            min_periods=CONFIG['rolling_min_periods']).mean()
    valid  = rolled.dropna()
    return float(valid.iloc[-1]) if len(valid) > 0 else float(series.mean())

