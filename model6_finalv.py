# ── Build basket lookup {antecedent → [rules]} ─────────────────────────────
basket_lookup = {}
for _, row in df_rules.iterrows():
    ant  = str(row['antecedents_str']).strip()
    con  = str(row['consequents_str']).strip()
    lift = float(row['lift'])
    comp = float(row['composite_score'])
    if ant not in basket_lookup:
        basket_lookup[ant] = []
    basket_lookup[ant].append({'consequent': con, 'lift': lift, 'composite_score': comp})

pf['basket_lift'] = pf['StockCode'].apply(
    lambda sc: max([r['lift'] for r in basket_lookup.get(sc, [{'lift': 1.0}])])
)

# FIX-5: threshold lowered 1.5 → 1.2 for better cross-sell coverage
pf['bundle_factor'] = np.where(
    pf['basket_lift'] > CONFIG['bundle_lift_threshold'],
    (1 + (pf['basket_lift'] - 1) * CONFIG['bundle_uplift_rate']).clip(
        upper=CONFIG['bundle_max_factor']
    ),
    1.0
)

pf['order_quantity']   = (pf['order_quantity_raw'] * pf['bundle_factor']).round(0)
pf['bundle_triggered'] = pf['basket_lift'] > CONFIG['bundle_lift_threshold']

basket_boost_map = {}
for sku in pf[pf['needs_reorder']]['StockCode']:
    for rule in basket_lookup.get(sku, []):
        con = rule['consequent']
        if con not in basket_boost_map:
            basket_boost_map[con] = 0.0
        basket_boost_map[con] = max(basket_boost_map[con], rule['composite_score'])

pf['bundle_received_boost'] = pf['StockCode'].map(basket_boost_map).fillna(0.0)

