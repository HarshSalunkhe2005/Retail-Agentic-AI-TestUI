"""## Section 9 — ML Risk Regression

**FIX-1 Applied:** `days_to_stockout` **removed** from `ML_FEATURES`.

Previously, `days_to_stockout = Available_Stock / forecast_demand` was used as a predictor, but the ML target (`stockout_risk_score`) was also directly derived from `Available_Stock` and `forecast_demand`. This created severe data leakage — the model was essentially predicting from itself.

- `days_to_stockout` is kept for **business logic** (reorder triggering, reason codes)
- It is **not** included in ML training or prediction features
- The target is still the composite risk score built from stock depth, urgency, and variability
"""

# ── Construct stockout risk score (regression target) ──────────────────────
# Composite formula combining three orthogonal signals:
#   50% → stock depth relative to reorder point
#   30% → urgency of stockout (days remaining vs. 30-day horizon)
#   20% → demand variability (CV)
# NOTE: days_to_stockout is derived from Available_Stock / forecast_demand,
# both of which overlap with the 50% and 30% terms. Keeping days_to_stockout
# as a feature would cause leakage — it is therefore EXCLUDED from ML_FEATURES.

pf['stockout_risk_score'] = (
    0.50 * (
        pf['final_reorder_point'] /
        (pf['Available_Stock'].astype(float).clip(lower=0.01) + pf['final_reorder_point'])
    ) +
    0.30 * (1 - pf['days_to_stockout'].clip(upper=30) / 30) +
    0.20 * (pf['demand_cv'].clip(0, 3) / 3)
).clip(0, 1)

# ── FIX-1: ML features — days_to_stockout REMOVED ─────────────────────────
ML_FEATURES = [
    'forecast_demand',
    'demand_std',
    'demand_cv',
    'demand_trend_recent',
    'zero_demand_ratio',
    'return_rate',
    'lead_time_days',
    'p_zero',
    'basket_lift',
    # 'days_to_stockout',  ← REMOVED: data leakage (FIX-1)
    'adi',
    'cv2',
    'segment_demand_weight',
    'bundle_received_boost',
]

print('[FIX-1] ML Features (days_to_stockout EXCLUDED to prevent data leakage):')
for f in ML_FEATURES:
    print(f'  + {f}')
print(f'  Total: {len(ML_FEATURES)} features (was 14, now 13)')

# ── Temporal train/test split ──────────────────────────────────────────────
pf_ml    = pf.dropna(subset=ML_FEATURES + ['stockout_risk_score'])
pf_ml    = pf_ml.sort_values('demand_days').reset_index(drop=True)
split_idx = int(len(pf_ml) * 0.80)
train_ml  = pf_ml.iloc[:split_idx]
test_ml   = pf_ml.iloc[split_idx:]

X_train = train_ml[ML_FEATURES].fillna(0)
y_train = train_ml['stockout_risk_score']
X_test  = test_ml[ML_FEATURES].fillna(0)
y_test  = test_ml['stockout_risk_score']

# Cost-sensitive sample weights
sample_weights = 1 + CONFIG['risk_penalty_weight'] * y_train

print(f'Train: {len(X_train):,} SKUs | Test: {len(X_test):,} SKUs')
print(f'Target range: [{y_train.min():.4f}, {y_train.max():.4f}]  mean: {y_train.mean():.4f}')

# ── GradientBoostingRegressor (algorithm unchanged) ────────────────────────
print('\nFitting GradientBoostingRegressor ...')
t0  = time.time()
gbr = GradientBoostingRegressor(
    n_estimators    = 200,
    learning_rate   = 0.05,
    max_depth       = 4,
    subsample       = 0.8,
    min_samples_leaf= 5,
    random_state    = 42
)
gbr.fit(X_train, y_train, sample_weight=sample_weights)
y_pred_test = gbr.predict(X_test).clip(0, 1)

mae  = mean_absolute_error(y_test, y_pred_test)
rmse = np.sqrt(mean_squared_error(y_test, y_pred_test))
r2   = r2_score(y_test, y_pred_test)

def smape(actual, pred):
    denom = (np.abs(actual) + np.abs(pred)).clip(min=1e-8)
    return float(np.mean(2 * np.abs(actual - pred) / denom) * 100)

def wape(actual, pred):
    return float(np.sum(np.abs(actual - pred)) / np.sum(np.abs(actual).clip(min=1e-8)) * 100)

sm = smape(y_test.values, y_pred_test)
wp = wape(y_test.values,  y_pred_test)

print(f'Done in {time.time()-t0:.1f}s')
print()
