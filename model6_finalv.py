"""## Section 10 — Purchase Order Decision Engine

**FIX-2 Applied:**
- Only SKUs with `priority == High OR Medium` are considered for reorder
- Additional cap: only the top `reorder_top_pct` (35%) SKUs by risk score actually get orders
- This reduces the unrealistic 81% reorder rate to a manageable ~30–40%

**FIX-6 Applied:**
- Greedy budget allocation: SKUs sorted by risk_score descending
- Orders selected until cumulative PO value hits `budget_limit_gbp`
- Ensures the system is financially constrained and business-realistic
"""

# ── Trigger reason builder ─────────────────────────────────────────────────
def build_reason(row) -> str:
    reasons = []
    avail = float(row.get('Available_Stock', 9999))
    ss    = float(row.get('final_safety_stock', 0))
    dts   = float(row.get('days_to_stockout', 9999))
    lt    = float(row.get('lead_time_days', 7))
    trend = float(row.get('demand_trend_recent', 0))
    lift  = float(row.get('basket_lift', 1.0))
    cls   = str(row.get('abc_class', 'C'))
    if avail <= ss:
        reasons.append('BELOW_SAFETY_STOCK')
    if dts <= lt:
        reasons.append('STOCKOUT_WITHIN_LEAD_TIME')
    if trend > 0.5:
        reasons.append('RISING_DEMAND_TREND')
    if lift > CONFIG['bundle_lift_threshold']:
        reasons.append('BUNDLE_RULE_TRIGGERED')
    if cls == 'A' and dts < 14:
        reasons.append('ABC_A_CRITICAL')
    return ' | '.join(reasons) if reasons else 'STANDARD_REORDER'

pf['reason'] = pf.apply(build_reason, axis=1)

# ── FIX-2: Priority-gated + top-N% reorder filter ─────────────────────────
# Step 1: Only High/Medium priority SKUs are eligible for reorder
