print(f'  After priority filter     : {eligible_mask.sum():,} SKUs')
print(f'  After top-{CONFIG["reorder_top_pct"]*100:.0f}% cap         : {pf["final_reorder_flag"].sum():,} SKUs '
      f'({pf["final_reorder_flag"].mean()*100:.1f}%)')

# ── PO value ──────────────────────────────────────────────────────────────
