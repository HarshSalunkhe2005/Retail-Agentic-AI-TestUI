"""## Section 14 — Kafka Event Payload: [Reorder_Recommendation_Created]"""

def build_kafka_payload(df_po: pd.DataFrame, top_n: int = 100) -> dict:
    """
    Builds the [Reorder_Recommendation_Created] Kafka event.
    Structure is backward compatible with v4.0 field names.
    v4.1 adds: budget_limit, fix_version in metadata.
    """
    top_orders = df_po.nlargest(top_n, 'Risk_Score')
    po_items   = []
    for _, row in top_orders.iterrows():
        po_items.append({
            'po_id'          : row['PO_ID'],
            'stock_code'     : row['StockCode'],
            'description'    : row['Description'],
            'category'       : row['Category'],
            'abc_class'      : row['ABC_Class'],
            'demand_type'    : row['Demand_Type'],
            'stock_on_hand'  : int(row['Stock_On_Hand']),
            'available_stock': int(row['Available_Stock']),
            'forecast_demand': round(float(row['Forecast_Demand']), 4),
            'reorder_point'  : round(float(row['Reorder_Point']), 0),
            'order_quantity' : int(row['Order_Quantity']),
            'po_value_gbp'   : round(float(row['PO_Value_GBP']), 2),
            'risk_score'     : round(float(row['Risk_Score']), 6),
            'priority'       : row['Priority'],
            'days_to_stockout': round(float(row['Days_To_Stockout']), 1),
            'lead_time_days' : int(row['Lead_Time_Days']),
            'basket_lift'    : round(float(row['Basket_Lift']), 4),
            'bundle_triggered': bool(row['Bundle_Triggered']),
            'warehouse'      : row['Warehouse'],
            'reason'         : row['Reason'],
        })

    return {
        'event_type'    : 'Reorder_Recommendation_Created',
        'agent_id'      : 'agent-07-inventory-reorder',
        'kafka_topic'   : 'retail.intelligence.inventory.reorder',
        'schema_version': '4.1.0',
        'generated_at'  : datetime.utcnow().strftime('%Y-%m-%dT%H:%M:%SZ'),
        'metadata': {
            'model_version'        : 'v4.1',
            'fix_version'          : 'fixes: leakage, over-reorder, demand-type, eoq, basket, budget',
            'total_skus_evaluated' : len(df_final),
            'active_pos_generated' : len(df_po),
            'total_po_value_gbp'   : round(float(df_po['PO_Value_GBP'].sum()), 2),
            'budget_limit_gbp'     : CONFIG['budget_limit_gbp'],
            'forecast_model'       : 'Prophet (MAPE=10.9%)',
            'risk_model'           : f'GBR Regression leakage-free (MAE={mae:.4f})',
            'risk_threshold_high'  : round(p90, 6),
            'risk_threshold_medium': round(p70, 6),
            'forecast_multiplier'  : round(fc_multiplier, 4),
            'forecast_confidence'  : round(fc_confidence, 4),
            'ml_features_count'    : len(ML_FEATURES),
            'bundle_lift_threshold': CONFIG['bundle_lift_threshold'],
            'moq'                  : CONFIG['moq'],
            'max_order_limit'      : CONFIG['max_order_limit'],
        },
        'purchase_orders'  : po_items,
        'consuming_agents' : ['erp-purchase-order-system', 'agent-01-pricing']
    }

kafka_payload = build_kafka_payload(df_po)
kafka_path    = os.path.join(DATA_DIR, 'kafka_reorder_payload_v4.json')
with open(kafka_path, 'w') as f:
    json.dump(kafka_payload, f, indent=2)

